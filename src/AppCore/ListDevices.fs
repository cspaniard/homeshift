module AppCore.ListDevices

open Microsoft.Extensions.DependencyInjection

open DI.Interfaces
open DI.Providers


//----------------------------------------------------------------------------------------------------------------------
let devicesService = serviceProvider.GetService<IDevicesService>()
//----------------------------------------------------------------------------------------------------------------------

//----------------------------------------------------------------------------------------------------------------------
let getDeviceList () =

    devicesService.getValidDevicesDataOrEx ()
//----------------------------------------------------------------------------------------------------------------------

//----------------------------------------------------------------------------------------------------------------------
module CLI =

    //------------------------------------------------------------------------------------------------------------------
    let showDeviceList () =

        getDeviceList ()
        |> devicesService.outputDevices
    //------------------------------------------------------------------------------------------------------------------
